class Main(tk.Tk):
    def __init__(self):
        tk.Tk.__init__(self)
        self.geometry("512x384")
        self.minsize(512,384)
        self.maxsize(512,384)
        self.title("Autoclicker v1.0")
        self.iconMagic()

        self.bold=font.Font(family="Javanese Text",size="12",weight="bold")

        container=tk.Frame(self)
        container.pack(side="top",fill="both",expand=True)
        container.grid_rowconfigure(0,weight=1)
        container.grid_columnconfigure(0,weight=1)

        self.frames = {}
        for F in (MainPage,Credits):
            page_name = F.__name__
            frame=F(parent=container,cont=self)
            self.frames[page_name]=frame

            frame.grid(row=0,column=0,sticky="nsew")

        self.show_frame("MainPage")

    def show_frame(self,page_name):
        frame = self.frames[page_name]
        frame.tkraise()

    def iconMagic(self):    #XDDDDDDDDDDDDDDDDDDDD, what great programming
        icondata = base64.b64decode(iconbase64)
        tempFile = "icon.ico"
        iconfile = open(tempFile,"wb")
        iconfile.write(icondata)
        iconfile.close()
        self.iconbitmap(tempFile)
        os.remove(tempFile)
